package game;

public class Player {

	private String Name;
	private int Id;

	public String getName() {
		return Name;
	}

	/**
	 * 
	 * @param value
	 */
	public void setName(String value) {
		Name = value;
	}

	public int getId() {
		return Id;
	}

	/**
	 * 
	 * @param value
	 */
	public void setId(int value) {
		Id = value;
	}

}